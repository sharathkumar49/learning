"""
LeetCode 1689. Partitioning Into Minimum Number Of Deci-Binary Numbers

Given a string n, return the minimum number of positive deci-binary numbers needed to sum up to n.

Example 1:
Input: n = "32"
Output: 3

Constraints:
- 1 <= n.length <= 10^5
- n consists of digits only.
"""

def minPartitions(n):
    return int(max(n))

